public class Locatie {
    //VARIABELEN
    private int x;
    private int y;

    //CONSTRUCTOR
    public Locatie(int x, int y){
    this.x = x;
    this.y = y;
    }

    //FUNCTIES
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
}
